package com.zsx.beans;

public class PostBarItem {
	
	private int postbarId;
	
	private String userPhone;
	
	private String nickName;
	
	private String title;
	
	private String content;
	
	private String createTime;
	
	private String imageUrl;
	
	private String headUrl;
	
	private String orders;
	
	private String commentNums;
	
	private String likeNums;
	
	private String hasLike;

	public int getPostbarId() {
		return postbarId;
	}

	public void setPostbarId(int postbarId) {
		this.postbarId = postbarId;
	}

	public String getUserPhone() {
		return userPhone;
	}

	public void setUserPhone(String userPhone) {
		this.userPhone = userPhone;
	}

	public String getNickName() {
		return nickName;
	}

	public void setNickName(String nickName) {
		this.nickName = nickName;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

	public String getImageUrl() {
		return imageUrl;
	}

	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}

	public String getHeadUrl() {
		return headUrl;
	}

	public void setHeadUrl(String headUrl) {
		this.headUrl = headUrl;
	}

	public String getOrders() {
		return orders;
	}

	public void setOrders(String orders) {
		this.orders = orders;
	}

	public String getCommentNums() {
		return commentNums;
	}

	public void setCommentNums(String commentNums) {
		this.commentNums = commentNums;
	}

	public String getLikeNums() {
		return likeNums;
	}

	public void setLikeNums(String likeNums) {
		this.likeNums = likeNums;
	}

	public String getHasLike() {
		return hasLike;
	}

	public void setHasLike(String hasLike) {
		this.hasLike = hasLike;
	}
	
	
	
}
